def get_data(symbol):
    try:
        t = yf.Ticker(symbol)
        h = t.history(period="2d")
        if not h.empty:
            c, p = h['Close'].iloc[-1], h['Close'].iloc[-2]
            r = ((c - p) / p) * 100
            return f"{c:.2f} ({r:+.2f}%)"
    except: return "N/A"
    return "N/A"

